void main()
{
	//datatype int
	int A = 20;
	int B = 30;
	
	print("A = 20");
	
  //another way of writing above statement is-
	print("A = $A");  //"$" represent value of that integer just like "&" in C
	print("B =  $B");
  
  //If we want value of more than 1 variable then set of variables must be inside "{ }" 
	print("A + B = ${A+B}");
}
