	// FOR Loop
  
	// WAP to find the all numbers between 1 to 100
  
  int i=0;
  
  while(i<=100) {
      	print(i);
		}

	// WAP to find the odd numbers between 1 to 100

	while(i<=100) {

		if ( i % 2 != 0) {
			print(i);
		}
	}
  
  // WAP to find the even numbers between 1 to 100

	while(i<=100) {

		if ( i % 2 == 0) {
			print(i);
		}
	 }
  }
